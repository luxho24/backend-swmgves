import { Schema, model } from "mongoose";

const clienteSchema = new Schema({
  nombre: {
    type: String,
    required: true,
  },
  apellido: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const Cliente = model("Cliente", clienteSchema);
export default Cliente;
