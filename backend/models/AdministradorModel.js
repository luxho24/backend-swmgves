import { Schema, model } from "mongoose";

const administradorSchema = new Schema({
  nombre: {
    type: String,
    required: true,
  },
  apellido: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const Administrador = model("Administrador", administradorSchema);
export default Administrador;
